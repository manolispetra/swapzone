// Render 48x64 pixel art SVG — bold outlines, full body
export function renderPixelSVG(nft, size = 240) {
  const rows  = nft.art.length;
  const cols  = nft.art[0].length;
  const cellW = size / cols;
  const cellH = (size * rows / cols);
  const totalH = cellH;
  let rects = "";
  nft.art.forEach((row, y) => {
    [...row].forEach((v, x) => {
      if (v === "." || v === " ") return;
      const idx = parseInt(v, 16);
      if (idx === 0) return;
      const col = nft.palette[idx - 1] || "#888";
      rects += `<rect x="${(x*cellW).toFixed(2)}" y="${(y*cellH/rows*cols).toFixed(2)}" width="${(cellW+0.5).toFixed(2)}" height="${(cellH/rows*cols+0.5).toFixed(2)}" fill="${col}"/>`;
    });
  });
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 ${size} ${size}" shape-rendering="crispEdges" preserveAspectRatio="xMidYMid meet"><rect width="${size}" height="${size}" fill="${nft.palette[0]}"/>${rects}</svg>`;
}

