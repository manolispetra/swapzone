// Render function - takes 32x32 grid, outputs crisp SVG
export function renderPixelSVG(nft, size = 256) {
  const rows  = nft.art.length;
  const cols  = nft.art[0].length;
  const cellW = size / cols;
  const cellH = size / rows;
  const bg    = nft.palette[0];

  let rects = "";
  nft.art.forEach((row, y) => {
    [...row].forEach((v, x) => {
      if (v === "." || v === "0") return;
      const idx = parseInt(v, 16) - 1; // hex digit: 1-F = palette index 0-14
      const col = nft.palette[idx] || "#888";
      const px  = (x * cellW).toFixed(1);
      const py  = (y * cellH).toFixed(1);
      const pw  = (cellW + 0.6).toFixed(1);
      const ph  = (cellH + 0.6).toFixed(1);
      rects += `<rect x="${px}" y="${py}" width="${pw}" height="${ph}" fill="${col}"/>`;
    });
  });

  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 ${size} ${size}" shape-rendering="crispEdges"><rect width="${size}" height="${size}" fill="${bg}"/>${rects}</svg>`;
}

